/**
 * 십성(十星) - 일간을 기준으로 다른 천간들과의 관계
 */
export enum TenStars {
  // 비견류 (같은 오행)
  BIGYEON = '비견',     // 比肩 - 같은 오행, 같은 음양
  GEOP = '겁재',        // 劫財 - 같은 오행, 다른 음양
  
  // 식상류 (나를 생하는 오행)
  SIGSIN = '식신',      // 食神 - 내가 생하는 오행, 같은 음양
  SANGKWAN = '상관',    // 傷官 - 내가 생하는 오행, 다른 음양
  
  // 재성류 (내가 극하는 오행)
  PYEONJAE = '편재',    // 偏財 - 내가 극하는 오행, 같은 음양
  JEONGJAE = '정재',    // 正財 - 내가 극하는 오행, 다른 음양
  
  // 관성류 (나를 극하는 오행)
  PYEONGWAN = '편관',   // 偏官(七殺) - 나를 극하는 오행, 같은 음양
  JEONGGWAN = '정관',   // 正官 - 나를 극하는 오행, 다른 음양
  
  // 인성류 (나를 생하는 오행)
  PYEONIN = '편인',     // 偏印 - 나를 생하는 오행, 같은 음양
  JEONGIN = '정인',     // 正印 - 나를 생하는 오행, 다른 음양
}
